# Tipos de datos, operadores y variables y como definirlas




# Tipos de datos: 
# 1- Numericos: van sin comillas
#     - Enteros (int)
#     - Coma flotante o decimales (float)
#     - Complejos

# 2- Textos: van con ""

# 3- Booleanos:
#     - True
#     - False

#  --------------------------------------------------------------------

# Operadores:
# 1- Aritmeticos:
#     - Suma +
#     - Resta -
#     - Multiplicacion *
#     - Division /
#     - Modulo %  (arroja el resto de una division)
#     - Exponente **  (devuelve la potencia de un valor numerico)
#     - Division entera //  (devuelve el numero entero de una division)

# 2- Comparacion:
#     - Igual que ==
#     - Diferente que !=
#     - Mayor que >
#     - Menos que <
#     - Mayor o igual que >=
#     - Menor o igual que <=



# 3- Logicos:
#     - AND
#     - OR
#     - NOT

# 4- Asignacion:
#     - Igual =
#     - Incremento +=
#     - Decremento -=
#     - *=
#     - /=
#     - %=
#     - **=
#     - //=

# 5- Especiales:
#     - IS
#     - IS NOT
#     - IN
#     - NOT IN

#  --------------------------------------------------------------------

