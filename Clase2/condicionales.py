# Condicionales: if= permite comparar dos o mas condiciones para ver si estas se cumplen o no. Si se cumplen 
# podemos decirle a nuestro programa que haga una cosa y si no se cumplen que haga otra

# Uso condicionales para practicar con los operadores de comparacion
# 2- Comparacion:
#     - Igual que ==
#     - Diferente que !=
#     - Mayor que >
#     - Menos que <
#     - Mayor o igual que >=
#     - Menor o igual que <=

numero1=5
numero2=7

if numero1>numero2:
    print("El numero 1 es mayor")
else:
    print("El numero 2 es mayor")

palabra1="televisor"
palabra2="computadora"

if palabra1==palabra2:
    print("Si es igual")
else:
    print("No es igual")

palabra3="Vaso"
numero3=10

if palabra3!=numero3:
    print("Si es diferente")
else:
    print("No es diferente")